import React, { useState, useEffect } from 'react';
import { View, Text, StyleSheet, ScrollView, TouchableOpacity, ActivityIndicator, RefreshControl, Modal, Alert } from 'react-native';
import { SafeAreaView } from 'react-native-safe-area-context';
import AsyncStorage from '@react-native-async-storage/async-storage';
import { useColorScheme } from '@/hooks/use-color-scheme';
import { useRouter } from 'expo-router';
import { getStudentAttendance, AttendanceRecord } from '@/src/services/student';
import { getClassAttendance, getClasses, getStudentsByClassSection, markSessionAttendance, AttendanceRecord as TeacherAttendanceRecord } from '@/src/services/teacher';
import { usePermissions } from '@/src/hooks/usePermissions';

export default function AttendanceScreen() {
  const colorScheme = useColorScheme();
  const isDark = colorScheme === 'dark';
  const styles = getStyles(isDark);
  const calendarStyles = getCalendarStyles(isDark);
  const { hasPermission } = usePermissions();
  const router = useRouter();

  const [attendanceRecords, setAttendanceRecords] = useState<AttendanceRecord[]>([]);
  const [stats, setStats] = useState({ 
    totalDays: 0, 
    presentDays: 0, 
    absentDays: 0, 
    attendancePercentage: 0,
    totalSessions: 0,
    presentSessions: 0,
    sessionAttendanceRate: 0
  });
  const [loading, setLoading] = useState(true);
  const [refreshing, setRefreshing] = useState(false);
  const [selectedMonth, setSelectedMonth] = useState(new Date());
  const [selectedDate, setSelectedDate] = useState(new Date().getDate());
  const [isTeacher, setIsTeacher] = useState<boolean>(false);
  const [selectedClass, setSelectedClass] = useState<string>('');
  const [selectedSection, setSelectedSection] = useState<string>('');
  const [classes, setClasses] = useState<any[]>([]);
  const [showClassSelector, setShowClassSelector] = useState<boolean>(false);
  const [showMarkAttendance, setShowMarkAttendance] = useState<boolean>(false);
  const [students, setStudents] = useState<any[]>([]);
  const [attendanceData, setAttendanceData] = useState<{[key: string]: 'present' | 'absent' | 'half_day'}>({});
  const [selectedSession, setSelectedSession] = useState<'morning' | 'afternoon'>('morning');
  const [attendanceDate, setAttendanceDate] = useState<Date>(new Date());

  const daysOfWeek = ['S', 'M', 'T', 'W', 'T', 'F', 'S'];

  const checkRole = async () => {
    try {
      const role = await AsyncStorage.getItem('role');
      setIsTeacher(role === 'teacher');
      
      if (role === 'teacher') {
        // Fetch classes for teacher
        const classData = await getClasses();
        setClasses(classData);
        
        // Set default class if available
        if (classData.length > 0 && !selectedClass) {
          setSelectedClass(classData[0].className);
          if (classData[0].sections && classData[0].sections.length > 0) {
            setSelectedSection(classData[0].sections[0].sectionName);
          }
        }
      }
    } catch (error) {
      console.error('Error checking role:', error);
    }
  };

  const fetchAttendance = async () => {
    try {
      // Clear ALL previous data immediately
      setAttendanceRecords([]);
      setStats({ 
        totalDays: 0, 
        presentDays: 0, 
        absentDays: 0, 
        attendancePercentage: 0,
        totalSessions: 0,
        presentSessions: 0,
        sessionAttendanceRate: 0
      });

      const role = await AsyncStorage.getItem('role');
      const isTeacherRole = role === 'teacher';

      if (isTeacherRole) {
        // Teacher: fetch class attendance
        if (!selectedClass || !selectedSection) {
          console.log('[ATTENDANCE] No class/section selected for teacher');
          return;
        }

        const startDate = new Date(selectedMonth.getFullYear(), selectedMonth.getMonth(), 1).toISOString().split('T')[0];
        const records = await getClassAttendance(selectedClass, selectedSection, startDate);
        
        setAttendanceRecords(records as AttendanceRecord[]);
        // Calculate stats for teacher view
        const totalDays = records.length;
        const presentDays = records.filter(r => r.status === 'present').length;
        setStats({
          totalDays,
          presentDays,
          absentDays: totalDays - presentDays,
          attendancePercentage: totalDays > 0 ? Math.round((presentDays / totalDays) * 100) : 0,
          totalSessions: 0,
          presentSessions: 0,
          sessionAttendanceRate: 0
        });
      } else {
        // Student: fetch own attendance
        const startDate = new Date(selectedMonth.getFullYear(), selectedMonth.getMonth(), 1).toISOString();
        const endDate = new Date(selectedMonth.getFullYear(), selectedMonth.getMonth() + 1, 0).toISOString();

        console.log('[ATTENDANCE] Fetching FRESH data for month:', selectedMonth.toLocaleDateString(), 'Range:', startDate, 'to', endDate);

        const { records, stats: attendanceStats } = await getStudentAttendance(startDate, endDate);

        console.log('[ATTENDANCE] Backend returned:', records.length, 'records');

        // Only use the exact records returned from backend - no additional filtering
        // The backend should already filter by date range
        const validRecords = records.filter(record => {
          // Only basic validation - ensure record has required fields
          return record && record.date && record.dateString;
        });

        console.log('[ATTENDANCE] Valid records after filtering:', validRecords.length);

        setAttendanceRecords(validRecords);
        setStats({
          totalDays: attendanceStats.totalDays,
          presentDays: attendanceStats.presentDays,
          absentDays: attendanceStats.absentDays,
          attendancePercentage: attendanceStats.attendancePercentage,
          totalSessions: attendanceStats.totalSessions || 0,
          presentSessions: attendanceStats.presentSessions || 0,
          sessionAttendanceRate: attendanceStats.sessionAttendanceRate || 0
        });
      }
    } catch (error) {
      console.error('Error fetching attendance:', error);
      // Ensure we clear data on error too
      setAttendanceRecords([]);
      setStats({ 
        totalDays: 0, 
        presentDays: 0, 
        absentDays: 0, 
        attendancePercentage: 0,
        totalSessions: 0,
        presentSessions: 0,
        sessionAttendanceRate: 0
      });
    } finally {
      setLoading(false);
      setRefreshing(false);
    }
  };

  useEffect(() => {
    checkRole();
  }, []);

  useEffect(() => {
    // FORCE clear all data when month changes
    setAttendanceRecords([]);
    setStats({ 
      totalDays: 0, 
      presentDays: 0, 
      absentDays: 0, 
      attendancePercentage: 0,
      totalSessions: 0,
      presentSessions: 0,
      sessionAttendanceRate: 0
    });
    setLoading(true);

    // Add small delay to ensure state is cleared before fetching
    const timer = setTimeout(() => {
      fetchAttendance();
    }, 100);

    return () => clearTimeout(timer);
  }, [selectedMonth, selectedClass, selectedSection]);

  const onRefresh = () => {
    setRefreshing(true);
    fetchAttendance();
  };

  const getCalendarDays = () => {
    const year = selectedMonth.getFullYear();
    const month = selectedMonth.getMonth();
    const firstDay = new Date(year, month, 1);
    const lastDay = new Date(year, month + 1, 0);
    const startDate = new Date(firstDay);
    startDate.setDate(startDate.getDate() - firstDay.getDay());

    const days = [];
    const currentDate = new Date(startDate);

    console.log('[CALENDAR] Building calendar for month:', month + 1, year);
    console.log('[CALENDAR] Available attendance records:', attendanceRecords.length);
    attendanceRecords.forEach(record => {
      console.log('[CALENDAR] Record available:', record.dateString, record.status);
    });

    for (let i = 0; i < 42; i++) {
      // Use local date string to avoid timezone issues
      const year = currentDate.getFullYear();
      const month_num = currentDate.getMonth();
      const day_num = currentDate.getDate();
      const dateString = `${year}-${String(month_num + 1).padStart(2, '0')}-${String(day_num).padStart(2, '0')}`;

      const isCurrentMonth = currentDate.getMonth() === month;
      const isToday = currentDate.toDateString() === new Date().toDateString();

      // STRICT matching - only show attendance if we have exact backend data
      let attendanceRecord = null;
      if (isCurrentMonth) {
        attendanceRecord = attendanceRecords.find(record => {
          const recordDateStr = record.dateString || record.date?.split('T')[0];
          const matches = recordDateStr === dateString;
          if (matches) {
            console.log('[CALENDAR] ✅ MATCHED attendance for date:', dateString, 'Day:', day_num, 'Status:', record.status);
          }
          return matches;
        });
      }

      days.push({
        date: currentDate.getDate(),
        dateString,
        isCurrentMonth,
        isToday,
        attendance: attendanceRecord || null
      });

      currentDate.setDate(currentDate.getDate() + 1);
    }

    // Summary of matched attendance dates
    const datesWithAttendance = days.filter(day => day.attendance !== null);
    console.log('[CALENDAR] Generated', days.length, 'calendar days');
    console.log('[CALENDAR] Dates with attendance:', datesWithAttendance.map(day => `${day.date} (${day.dateString})`));

    return days;
  };

  const changeMonth = (direction: number) => {
    const newMonth = new Date(selectedMonth.getFullYear(), selectedMonth.getMonth() + direction, 1);
    setSelectedMonth(newMonth);
  };

  const getDateStyle = (day: { isCurrentMonth: boolean; isToday: boolean }) => {
    const composed = [calendarStyles.dateText];
    if (!day.isCurrentMonth) composed.push(calendarStyles.otherMonthText);
    if (day.isToday) composed.push(calendarStyles.todayText);
    return composed;
  };

  const getDateContainerStyle = (day: { date: number; isCurrentMonth: boolean }) => {
    const composed = [calendarStyles.dateContainer];
    if (day.date === selectedDate && day.isCurrentMonth) composed.push(calendarStyles.selectedDate);
    return composed;
  };

  const monthYear = selectedMonth.toLocaleDateString('en-US', { month: 'long', year: 'numeric' });

  if (loading) {
    return (
      <SafeAreaView style={styles.container} edges={['top']}>
        <View style={[styles.container, { justifyContent: 'center', alignItems: 'center' }]}>
          <ActivityIndicator size="large" color="#60A5FA" />
          <Text style={[styles.headerSubtitle, { marginTop: 12 }]}>Loading attendance...</Text>
        </View>
      </SafeAreaView>
    );
  }

  return (
    <SafeAreaView style={styles.container} edges={['top']}>
      <ScrollView
        style={styles.scrollView}
        showsVerticalScrollIndicator={false}
        refreshControl={<RefreshControl refreshing={refreshing} onRefresh={onRefresh} tintColor="#60A5FA" />}
      >
        <View style={styles.header}>
          <Text style={styles.headerTitle}>Attendance</Text>
          {isTeacher ? (
            <View style={styles.teacherControls}>
              <TouchableOpacity 
                style={styles.classSelector}
                onPress={() => setShowClassSelector(true)}
              >
                <Text style={styles.classSelectorText}>
                  {selectedClass && selectedSection ? `${selectedClass} - ${selectedSection}` : 'Select Class'}
                </Text>
                <Text style={styles.classSelectorIcon}>▼</Text>
              </TouchableOpacity>
              {hasPermission('markAttendance') && (
                <TouchableOpacity 
                  style={styles.markAttendanceButton}
                  onPress={async () => {
                    if (!selectedClass) {
                      Alert.alert('Select Class', 'Please select a class first');
                      return;
                    }
                    // Fetch students for the selected class
                    try {
                      const studentsData = await getStudentsByClassSection(selectedClass, selectedSection === 'ALL' ? undefined : selectedSection);
                      setStudents(studentsData);
                      setAttendanceData({});
                      setShowMarkAttendance(true);
                    } catch (error) {
                      Alert.alert('Error', 'Failed to load students');
                    }
                  }}
                >
                  <Text style={styles.markAttendanceButtonText}>Mark Attendance</Text>
                </TouchableOpacity>
              )}
            </View>
          ) : (
            <Text style={styles.headerSubtitle}>
              {new Date().toLocaleDateString('en-US', { day: 'numeric', month: 'long', year: 'numeric' })}
            </Text>
          )}
        </View>

        <View style={styles.section}>
          <View style={styles.calendarCard}>
            <View style={styles.calendarHeader}>
              <TouchableOpacity style={styles.navButton} onPress={() => changeMonth(-1)}>
                <Text style={styles.navButtonText}>‹</Text>
              </TouchableOpacity>
              <Text style={styles.monthYear}>{monthYear}</Text>
              <TouchableOpacity style={styles.navButton} onPress={() => changeMonth(1)}>
                <Text style={styles.navButtonText}>›</Text>
              </TouchableOpacity>
            </View>

            <View style={calendarStyles.daysOfWeekContainer}>
              {daysOfWeek.map((day, index) => (
                <View key={index} style={calendarStyles.dayOfWeekCell}>
                  <Text style={calendarStyles.dayOfWeekText}>{day}</Text>
                </View>
              ))}
            </View>

            <View style={calendarStyles.calendarGrid}>
              {getCalendarDays().map((day, index) => (
                <TouchableOpacity
                  key={index}
                  style={getDateContainerStyle(day)}
                  onPress={() => day.isCurrentMonth && setSelectedDate(day.date)}
                >
                  <Text style={getDateStyle(day)}>{day.date}</Text>
                  {day.isCurrentMonth && day.attendance && (
                    <View style={calendarStyles.statusDot}>
                      {/* Morning session dot */}
                      <View style={[calendarStyles.dot, {
                        backgroundColor: 
                          day.attendance?.sessions?.morning?.status === 'present' ? '#4ADE80' :
                          day.attendance?.sessions?.morning?.status === 'absent' ? '#EF4444' :
                          day.attendance?.sessions?.morning === null ? '#D1D5DB' :
                          day.attendance?.status === 'present' ? '#4ADE80' :
                          day.attendance?.status === 'absent' ? '#EF4444' :
                          day.attendance?.status === 'no-class' ? '#9CA3AF' :
                          '#F3F4F6'
                      }]} />
                      {/* Afternoon session dot */}
                      <View style={[calendarStyles.dot, {
                        backgroundColor: 
                          day.attendance?.sessions?.afternoon?.status === 'present' ? '#4ADE80' :
                          day.attendance?.sessions?.afternoon?.status === 'absent' ? '#EF4444' :
                          day.attendance?.sessions?.afternoon === null ? '#D1D5DB' :
                          day.attendance?.status === 'present' ? '#4ADE80' :
                          day.attendance?.status === 'absent' ? '#EF4444' :
                          day.attendance?.status === 'no-class' ? '#9CA3AF' :
                          '#F3F4F6'
                      }]} />
                    </View>
                  )}
                </TouchableOpacity>
              ))}
            </View>

            <View style={styles.legendContainer}>
              <View style={styles.legendItem}>
                <View style={[styles.legendDot, { backgroundColor: '#4ADE80' }]} />
                <Text style={styles.legendText}>Present</Text>
              </View>
              <View style={styles.legendItem}>
                <View style={[styles.legendDot, { backgroundColor: '#EF4444' }]} />
                <Text style={styles.legendText}>Absent</Text>
              </View>
              <View style={styles.legendItem}>
                <View style={[styles.legendDot, { backgroundColor: '#D1D5DB' }]} />
                <Text style={styles.legendText}>No Session</Text>
              </View>
              <View style={styles.legendItem}>
                <View style={[styles.legendDot, { backgroundColor: '#9CA3AF' }]} />
                <Text style={styles.legendText}>No Class</Text>
              </View>
            </View>

            <View style={styles.sessionLegendContainer}>
              <Text style={styles.sessionLegendTitle}>Session Indicators:</Text>
              <View style={styles.sessionLegendRow}>
                <Text style={styles.sessionLegendText}>Left dot: Morning • Right dot: Afternoon</Text>
              </View>
            </View>

            <View style={styles.divider} />
            <Text style={styles.sessionInfo}>Attendance tracking for {monthYear}</Text>
            {attendanceRecords.length === 0 && (
              <View style={styles.noRecordsContainer}>
                <Text style={styles.noRecordsText}>No attendance records found</Text>
                <Text style={styles.noRecordsSubtext}>Your attendance will appear here once your teacher starts marking attendance</Text>
              </View>
            )}
          </View>
        </View>

        <View style={styles.section}>
          <Text style={styles.sectionTitle}>Attendance Overview</Text>
          <View style={styles.overviewCard}>
            <View style={styles.attendanceCircleContainer}>
              <View style={styles.attendanceCircle}>
                <View style={styles.circleInner}>
                  <Text style={styles.attendancePercentage}>
                    {(stats.attendancePercentage || 0).toFixed(0)}%
                  </Text>
                </View>
              </View>
            </View>
            <View style={styles.attendanceStats}>
              <View style={styles.attendanceStat}>
                <View style={[styles.statusDot, { backgroundColor: '#4ADE80' }]} />
                <View>
                  <Text style={styles.attendanceStatLabel}>Attended</Text>
                  <Text style={styles.attendanceStatValue}>
                    {stats.presentDays}/{stats.totalDays} days
                  </Text>
                  {stats.totalSessions !== undefined && (
                    <Text style={styles.attendanceStatValue}>
                      {stats.presentSessions}/{stats.totalSessions} sessions
                    </Text>
                  )}
                </View>
              </View>
            </View>
          </View>
        </View>

        <View style={{ height: 20 }} />
      </ScrollView>

      {/* Attendance Marking Modal */}
      <Modal visible={showMarkAttendance} transparent={true} animationType="slide">
        <View style={styles.modalContainer}>
          <View style={styles.modalContent}>
            <View style={styles.modalHeader}>
              <Text style={styles.modalTitle}>Mark Attendance</Text>
              <Text style={styles.modalSubtitle}>
                {selectedClass} - {selectedSection}
              </Text>
              
              {/* Date and Session Selection */}
              <View style={styles.attendanceControls}>
                <View style={styles.controlGroup}>
                  <Text style={styles.controlLabel}>Date:</Text>
                  <TouchableOpacity 
                    style={styles.dateButton}
                    onPress={() => {
                      // For simplicity, just use today's date
                      setAttendanceDate(new Date());
                    }}
                  >
                    <Text style={styles.dateButtonText}>
                      {attendanceDate.toLocaleDateString()}
                    </Text>
                  </TouchableOpacity>
                </View>
                
                <View style={styles.controlGroup}>
                  <Text style={styles.controlLabel}>Session:</Text>
                  <View style={styles.sessionButtons}>
                    <TouchableOpacity
                      style={[
                        styles.sessionButton,
                        selectedSession === 'morning' && styles.sessionButtonActive
                      ]}
                      onPress={() => setSelectedSession('morning')}
                    >
                      <Text style={[
                        styles.sessionButtonText,
                        selectedSession === 'morning' && styles.sessionButtonTextActive
                      ]}>Morning</Text>
                    </TouchableOpacity>
                    <TouchableOpacity
                      style={[
                        styles.sessionButton,
                        selectedSession === 'afternoon' && styles.sessionButtonActive
                      ]}
                      onPress={() => setSelectedSession('afternoon')}
                    >
                      <Text style={[
                        styles.sessionButtonText,
                        selectedSession === 'afternoon' && styles.sessionButtonTextActive
                      ]}>Afternoon</Text>
                    </TouchableOpacity>
                  </View>
                </View>
              </View>
            </View>
            
            <ScrollView style={styles.studentsList}>
              {students.map((student) => (
                <View key={student.userId} style={styles.studentRow}>
                  <Text style={styles.studentName}>
                    {student.name?.displayName || `${student.name?.firstName || ''} ${student.name?.lastName || ''}`.trim() || student.userId}
                  </Text>
                  <View style={styles.attendanceButtons}>
                    {['present', 'absent', 'half_day'].map((status) => (
                      <TouchableOpacity
                        key={status}
                        style={[
                          styles.attendanceButton,
                          attendanceData[student.userId] === status && styles.attendanceButtonSelected,
                          status === 'present' && styles.presentButton,
                          status === 'absent' && styles.absentButton,
                          status === 'half_day' && styles.halfDayButton,
                        ]}
                        onPress={() => setAttendanceData(prev => ({
                          ...prev,
                          [student.userId]: status as 'present' | 'absent' | 'half_day'
                        }))}
                      >
                        <Text style={[
                          styles.attendanceButtonText,
                          attendanceData[student.userId] === status && styles.attendanceButtonTextSelected
                        ]}>
                          {status === 'present' ? 'P' : status === 'absent' ? 'A' : 'H'}
                        </Text>
                      </TouchableOpacity>
                    ))}
                  </View>
                </View>
              ))}
            </ScrollView>
            
            <View style={styles.modalActions}>
              <TouchableOpacity 
                style={styles.cancelButton}
                onPress={() => setShowMarkAttendance(false)}
              >
                <Text style={styles.cancelButtonText}>Cancel</Text>
              </TouchableOpacity>
              <TouchableOpacity 
                style={styles.saveButton}
                onPress={async () => {
                  try {
                    const attendanceRecords = Object.entries(attendanceData).map(([studentId, status]) => ({
                      studentId,
                      status,
                      date: attendanceDate.toISOString(),
                      session: selectedSession
                    }));
                    
                    const success = await markSessionAttendance({
                      className: selectedClass,
                      section: selectedSection === 'ALL' || !selectedSection ? undefined : selectedSection,
                      date: attendanceDate.toISOString(),
                      session: selectedSession,
                      attendance: attendanceRecords
                    });
                    
                    if (success) {
                      Alert.alert('Success', 'Attendance marked successfully');
                      setShowMarkAttendance(false);
                      fetchAttendance(); // Refresh attendance data
                    } else {
                      Alert.alert('Error', 'Failed to mark attendance');
                    }
                  } catch (error) {
                    Alert.alert('Error', 'Failed to mark attendance');
                  }
                }}
              >
                <Text style={styles.saveButtonText}>Save Attendance</Text>
              </TouchableOpacity>
            </View>
          </View>
        </View>
      </Modal>
    </SafeAreaView>
  );
}

